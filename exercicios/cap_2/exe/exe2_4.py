#formatando strings

name = 'marcos augusto'
print(F'Alô {name.lower()}, você gostaria de aprender um pouco de Python hoje?')
print(F'Alô {name.upper()}, você gostaria de aprender um pouco de Python hoje?')
print(F'Alô {name.title()}, você gostaria de aprender um pouco de Python hoje?')

# método lower faz com que a str seja armazenada em minúsculo
# método upper maíusculo
# método title iniciais maiusculo 